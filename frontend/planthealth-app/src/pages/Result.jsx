import React, { useRef, useState } from 'react';
import { ArrowLeft, Download, Loader2 } from 'lucide-react';
import { Header } from '../components/Header';
import { ImagePreview } from '../components/ImagePreview';
import { Summary } from '../components/Summary';
import { ResultCard } from '../components/ResultCard';
import html2canvas from 'html2canvas';
import jsPDF from 'jspdf';
import { Capacitor } from '@capacitor/core';
import { Filesystem, Directory } from '@capacitor/filesystem';
import { Share } from '@capacitor/share';

export const Result = ({ result, previewUrl, onBack, onHelpClick, onHomeClick }) => {
  const { summary, leaves, image } = result || {};
  const reportRef = useRef(null);
  const [isDownloading, setIsDownloading] = useState(false);

  const handleDownload = async () => {
    if (!reportRef.current) return;
    setIsDownloading(true);

    try {
      // Tomamos una "foto" del contenedor del reporte
      const canvas = await html2canvas(reportRef.current, {
        scale: 2, // Mejor calidad para el PDF
        useCORS: true,
      });

      const imgData = canvas.toDataURL('image/jpeg', 0.95);
      
      // Creamos el PDF en tamaño A4 (210mm x 297mm)
      const pdf = new jsPDF({
        orientation: 'portrait',
        unit: 'mm',
        format: 'a4'
      });
      
      const pdfWidth = pdf.internal.pageSize.getWidth();
      const pdfHeight = (canvas.height * pdfWidth) / canvas.width;
      
      pdf.addImage(imgData, 'JPEG', 0, 0, pdfWidth, pdfHeight);

      if (Capacitor.isNativePlatform()) {
        // En dispositivo móvil, usamos Filesystem y Share
        const base64Data = pdf.output('datauristring').split(',')[1];
        
        const savedFile = await Filesystem.writeFile({
          path: `PlantHealth_Report_${Date.now()}.pdf`,
          data: base64Data,
          directory: Directory.Cache
        });
        
        await Share.share({
          title: 'Plant Health Report',
          text: 'Here is the health report for my plant generated by PlantsHealthy.',
          url: savedFile.uri,
          dialogTitle: 'Share Report'
        });
      } else {
        // En navegador web, descargamos directo
        pdf.save('PlantHealth_Report.pdf');
      }
    } catch (error) {
      console.error('Error generating PDF:', error);
      alert('There was an error generating the report. Please try again.');
    } finally {
      setIsDownloading(false);
    }
  };

  return (
    <>
      <Header onHelpClick={onHelpClick} onHomeClick={onHomeClick} />
      <main className="w-full pt-[88px] pb-[100px] px-4 flex flex-col gap-6 max-w-3xl mx-auto">
        {/* Header / Back Context */}
        <div className="flex items-center gap-2 pt-4">
          <button 
            onClick={onBack}
            className="w-10 h-10 rounded-full bg-surface-container flex items-center justify-center text-primary hover:bg-surface-container-high transition-colors"
          >
            <ArrowLeft className="w-5 h-5" />
          </button>
          <h2 className="font-bold text-[24px] leading-8 text-on-background">Scan Results</h2>
        </div>

        {/* Contenedor que será convertido a PDF */}
        <div ref={reportRef} className="flex flex-col gap-6 bg-background dark:bg-[#0d1b11] p-4 -mx-4 rounded-xl">
          {/* Results Image Container */}
          <ImagePreview imageUrl={previewUrl} annotatedImageBase64={image} />

          {/* Summary Section */}
          {summary && (
            <Summary healthy={summary.healthy} diseased={summary.diseased} />
          )}

          {/* Details Section */}
          <section className="flex flex-col gap-4">
            <h3 className="font-semibold text-[20px] text-on-background dark:text-white">Leaves Detected</h3>
            <div className="flex flex-col gap-2">
              {leaves && leaves.length > 0 ? (
                leaves.map((leaf, index) => (
                  <ResultCard key={index} leaf={leaf} />
                ))
              ) : (
                <div className="text-on-surface-variant text-sm p-4 text-center bg-surface-container-lowest rounded-lg border border-outline-variant/30">
                  No distinct leaves detected.
                </div>
              )}
            </div>
          </section>
        </div>

        {/* Action Button */}
        <div className="pt-3 pb-8">
          <button 
            onClick={handleDownload}
            disabled={isDownloading}
            className="w-full bg-primary hover:bg-primary-container text-on-primary font-semibold text-[14px] rounded-full py-4 px-6 flex items-center justify-center gap-2 transition-all shadow-[0_4px_15px_-3px_rgba(6,95,70,0.3)] disabled:opacity-70"
          >
            {isDownloading ? (
              <Loader2 className="w-5 h-5 animate-spin" />
            ) : (
              <Download className="w-5 h-5" />
            )}
            {isDownloading ? 'Generating PDF...' : 'Download Full Report'}
          </button>
        </div>
      </main>
    </>
  );
};
